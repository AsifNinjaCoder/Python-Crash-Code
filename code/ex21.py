def add(a,b):
    print(f" Adding {a}+{b} result{a+b}")
    return a+b

def subtract(a,b):
    print(f"SUBSRACTING {a} - {b}")
    return a-b

def multiply(a,b):
    print(f"MULTIPLYING {a} *{b}")
    return a+b 

age = add(3,5)
height = subtract(78,4)
weight = multiply(90,8)

print (f"Age:{age} Height:{height} Weight:{weight}")

